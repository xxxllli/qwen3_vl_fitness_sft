from __future__ import annotations

import argparse
from pathlib import Path

from qwen3vl_trigger.utils.config import load_config

SENTINEL_START = '# >>> FITNESS_TRIGGER_DATASETS_AUTOGENERATED >>>'
SENTINEL_END = '# <<< FITNESS_TRIGGER_DATASETS_AUTOGENERATED <<<'


def register_dataset(cfg: dict, split: str = 'train') -> Path:
    repo = Path(cfg['paths']['qwen3vl_repo_dir']).resolve()
    init_path = repo / 'qwen-vl-finetune' / 'qwenvl' / 'data' / '__init__.py'
    if not init_path.exists():
        raise FileNotFoundError(
            f'{init_path} not found. Run scripts/setup_official_qwen3vl.sh first, or set paths.qwen3vl_repo_dir correctly.'
        )
    out_dir = Path(cfg['paths']['output_dir']).resolve()
    train_json = out_dir / 'official_json' / 'train.json'
    val_json = out_dir / 'official_json' / 'val.json'
    test_json = out_dir / 'official_json' / 'test.json'
    for p in [train_json, val_json, test_json]:
        if not p.exists():
            raise FileNotFoundError(f'Missing dataset file: {p}. Run build_dataset first.')

    block = f'''
{SENTINEL_START}
FITNESS_TRIGGER_TRAIN = {{"annotation_path": r"{train_json}", "data_path": ""}}
FITNESS_TRIGGER_VAL = {{"annotation_path": r"{val_json}", "data_path": ""}}
FITNESS_TRIGGER_TEST = {{"annotation_path": r"{test_json}", "data_path": ""}}
try:
    data_dict.update({{
        "fitness_trigger_train": FITNESS_TRIGGER_TRAIN,
        "fitness_trigger_val": FITNESS_TRIGGER_VAL,
        "fitness_trigger_test": FITNESS_TRIGGER_TEST,
    }})
except NameError:
    data_dict = {{
        "fitness_trigger_train": FITNESS_TRIGGER_TRAIN,
        "fitness_trigger_val": FITNESS_TRIGGER_VAL,
        "fitness_trigger_test": FITNESS_TRIGGER_TEST,
    }}
{SENTINEL_END}
'''
    text = init_path.read_text(encoding='utf-8')
    if SENTINEL_START in text and SENTINEL_END in text:
        before = text.split(SENTINEL_START)[0].rstrip()
        after = text.split(SENTINEL_END)[-1].lstrip()
        text = before + '\n\n' + block + '\n' + after
    else:
        text = text.rstrip() + '\n\n' + block + '\n'
    init_path.write_text(text, encoding='utf-8')
    print(f'Registered datasets in {init_path}')
    return init_path


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument('--config', default='configs/default.yaml')
    args = parser.parse_args()
    register_dataset(load_config(args.config))


if __name__ == '__main__':
    main()
